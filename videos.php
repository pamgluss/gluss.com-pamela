					<h2> Animations</h2>
					<h3> Senior Thesis: Queen Bee</h3>
					<iframe src="https://player.vimeo.com/video/129923617" width="750" height="422" frameborder="0" webkitallowfullscreen mozallowfullscreen allowfullscreen></iframe>
					<p>Password is BEES</p>
					<p>I started my senior thesis in January of 2014. During preproduction and story development, I knew I wanted a story that was funny and gag-heavy. Initially this film was about bees who stole hairspray and accidentally grabbed bug spray on accident.</p>
					<p>This film was animated in TVPaint and inked and colored in ToonBoom Animate Pro. Some background bees were made as puppets in Photoshop and AfterEffects and composited in using Premiere. All sound and video editing was done in Premiere. </p>
					<p>I must thank my countless worker bees who helped ink, color, arrange sound and place music:</p>
					<p>Inking: Izzy Iampieri, Mercury Carli, Nick Love and Elizabeth Sheffield.</p>
					<p>Coloring: <a href="https://www.linkedin.com/in/courtney-getman-32500b67">Courtney Getman</a>, <a href="http://dustinraimondi.com/">Dustin Raimondi</a>, <a href="https://ssmithcis.net/">Sebastian Smith</a>, Nick Love and Elizabeth Sheffield.</p>
					<p>Help with AfterEffects and the bee puppets came from <a href="http://www.miguelmirasol.com/#!animation/ch12">Miguel Mirasol.</a> Sound editing help came from Nick Love. My composer was Nate Wren.</p>
					<p> And of course, the film wouldn't have been possible whatsoever without the dedication and direction of my thesis advisor, Peter Murphey.</p>
					
					

